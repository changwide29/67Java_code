package W12;

import java.util.Scanner;

public class W12_02_WhileTrue_KB {
    public static void main(String[] args) {
        Scanner kb = new Scanner(System.in);

        while (true) {
            System.out.print("โปรดป้อนตัวเลข : ");
            int number = kb.nextInt();

            if (number == 0) {
                break;
            }
        }

    // วนลูปไปเรื่อยๆ ใช้ while (true) 
        // แสดงข้อความ Enter Number :
        // รับค่าจากคีย์บอร์ดเก็บไว้ในตัวแปร number
                
        // ตรวจสอบเงื่อนไข ถ้า number เท่ากับ 0 ให้ออกจากลูป
            // ออกจากลูป

        
    }
}